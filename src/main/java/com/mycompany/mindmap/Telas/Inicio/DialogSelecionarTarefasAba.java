    private List<Aba> pegarAbasDoBanco(int idUsuario) {
        List<Aba> abas = new ArrayList<>();
        

        String sql = "SELECT * FROM aba WHERE idUsuario = ?";

        try {
            Connection conn = ConexaoBD.obtemConexao();
            PreparedStatement consulta = conn.prepareStatement(sql);
            consulta.setInt(1, idUsuario);
            

            ResultSet rs = consulta.executeQuery();

            while (rs.next()) {
                String tituloAba = rs.getString("titulo");
                int id = rs.getInt("idAba");
                abas.add(new Aba(id, tituloAba));
            }

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

        return abas;
    }

    public DialogSelecionarTarefasAba(java.awt.Frame parent, boolean modal, int idUsuario) {
        super(parent, modal);
        initComponents();
        setLocationRelativeTo(null);
        idUsuarioLogado = idUsuario;

        try {
            List<Aba> abas = pegarAbasDoBanco(idUsuarioLogado);
            for (Aba aba : abas) {
                comboBoxAbas.addItem(aba.getTitulo());

                comboBoxAbas.addActionListener(e -> {

                    int indexSelecionado = comboBoxAbas.getSelectedIndex();
                    if (indexSelecionado >= 0) {
                        Aba abaSelecionada = abas.get(indexSelecionado);
                        txtIdAba.setText(String.valueOf(abaSelecionada.getIdAba()));
                    }

                });                
            }
            System.out.println(idUsuarioLogado);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
   
    }

